package dynamicplan

import "fmt"

//斐波那契数，通常用 F(n) 表示，形成的序列称为 斐波那契数列 。该数列由 0 和 1 开始，后面的每一项数字都是前面两项数字的和。也就是： F(0) = 0，F(1) = 1 F(n) = F(n - 1) + F(n - 2)，其中 n > 1 给你n ，请计算 F(n) 。
//示例 1： 输入：2 输出：1
//示例 2： 输入：3 输出：2
//示例 3： 输入：4 输出：3

func fib(n int) int {
	if n == 0 {
		return 0
	}
	if n == 1 {
		return 1
	}
	return fib(n-1) + fib(n-2)
}
func Handle2() {
	fmt.Println(fib(6))
}
